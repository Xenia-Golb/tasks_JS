/*
IPv4 — это числовой идентификатор, используемый для адресации устройств в сети Интернет. Он состоит из четырёх октетов, каждый из которых представляет собой число от 0 до 255, разделённых точками. Например: 192.168.0.1.

Напишите функцию isValidIP(str), которая принимает строку и определяет, является ли переданная строка правильным IPv4 адресом согласно описанному выше формату.

Функция должна вернуть true, если срока является IP-адресом, иначе вернуть false.

*/
function isValidIP(str) {
    // Разбиваем строку на массив чисел по точкам
    const parts = str.split('.');

    // Проверяем, что массив содержит 4 числа
    if (parts.length !== 4) {
        return false;
    }

    // Проверяем, что все числа из массива находятся в диапазоне от 0 до 255
    for (const part of parts) {
        const num = parseInt(part, 10);
        if (isNaN(num) || num < 0 || num > 255) {
            return false;
        }
    }

    return true;
}

// Проверка работы функции
console.log(isValidIP('192.168.0.1'));
console.log(isValidIP('1.2.3.4'));
console.log(isValidIP('1.2.3.4.5'));